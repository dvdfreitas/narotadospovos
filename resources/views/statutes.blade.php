        <h1>Estatutos</h1>


        <p class="text-2xl text-nrp-blue py-4 text-center">DENOMINAÇÃO</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 1º</p>

        <p>Sob a denominação de “NA ROTA DOS POVOS” ASSOCIAÇÃO PARA A EDUCAÇÃO E COOPERAÇÃO, ou pela forma abreviada “NA ROTA DOS POVOS” é criada esta associação civil sem fins lucrativos, que se regerá por estes ESTATUTOS, e pelas normas legais aplicáveis.</p>



        <p class="text-2xl text-nrp-blue py-4 text-center">SEDE</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 2º</p>

        <p>A Associação terá sede na Rua Gonçalves Zarco, 2644, Santa Cruz do Bispo, 4485-821 Matosinhos, sendo criada por tempo indeterminado, podendo abrir filiais ou delegações em outros locais do território nacional ou estrangeiro.</p>



        <p class="text-2xl text-nrp-blue py-4 text-center">OBJECTO</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 3 º</p>

        <p>A “Na Rota dos Povos” tem por objeto apoiar e desenvolver ações para a defesa, elevação e manutenção da qualidade de vida do ser humano e do meio ambiente, através do desenvolvimento de atividades de carácter educativo, social, cultural, ambiental e desportivo, bem como da cooperação, do diálogo intercultural e do apoio direto e efetivo a projetos de organizações congéneres nacionais ou internacionais, nomeadamente através da conceção, execução, facilitação e realização de programas nas seguintes áreas privilegiadas:</p>
        <br>
        1 – Áreas privilegiadas:
        <ul class="list-disc font-thin space-y-1">
            <li class="list-item list-inside">Educação como sendo o único caminho para o desenvolvimento,</li>
            <li class="list-item list-inside">Proteção e promoção da ética, da paz, da cidadania, dos direitos humanos, da democracia e de outros valores,</li>
            <li class="list-item list-inside">Promoção dos direitos das pessoas portadoras de quaisquer necessidades especiais ou deficiência;</li>
            <li class="list-item list-inside">Promoção dos direitos humanos, em especial os direitos da mulher e da criança.</li>
            <li class="list-item list-inside">Combate a todo o tipo de discriminação sexual, racial e social, trabalho forçado e infantil;</li>
            <li class="list-item list-inside">Integração social de pessoas com necessidades especiais ou outras,</li>
            <li class="list-item list-inside">Assistência social e humanitária aos mais desfavorecidos,</li>
            <li class="list-item list-inside">Apoio à saúde e nutrição,</li>
            <li class="list-item list-inside">Apoio ao desenvolvimento de pequenos projetos,</li>
            <li class="list-item list-inside">Preservação, defesa e conservação do meio ambiente e promoção do desenvolvimento sustentável;</li>
            <li class="list-item list-inside">Promoção da atividade desportiva,</li>
            <li class="list-item list-inside">Promoção do voluntariado, de criação de estágios, formação de jovens e sua colocação no mercado de trabalho;</li>
            <li class="list-item list-inside">Sensibilização da opinião pública com vista ao desenvolvimento da cooperação, promoção de debates, de exposições e de outras atividades inseridas no projeto</li>
        </ul>
        2) Desenvolvimento das atividades:

        <p>As atividades acima previstas desenvolver-se-ão mediante a execução direta de projetos, programas, planos de ações, por meio da doação de recursos físicos, humanos e financeiros, ou ainda pela prestação de serviços de apoio a outras organizações sem fins lucrativos e a órgãos do sector público que atuem em áreas afins.</p>

        <p class="text-2xl text-nrp-blue py-4 text-center">INDEPENDÊNCIA</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 4º</p>

        <p>A Na Rota dos Povos desenvolverá a sua atividade de forma autónoma face a quaisquer organizações de carácter político, religioso ou económico, de modo a manter os princípios da imparcialidade, neutralidade, independência, integridade, boa governança, isenção e confidencialidade na comunicação, transparência e prestação de contas.</p>
        <p>É vedada à “NA ROTA DOS POVOS”, como organização não governamental e pessoa coletiva de utilidade pública, a participação em campanhas de interesse político-partidário ou eleitorais, sob quaisquer meios ou formas.</p>
        <p>A “NA ROTA DOS POVOS” em observância dos princípios da legalidade, impessoalidade, moralidade, publicidade, economicidade e da eficiência, adotará práticas de gestão administrativa, necessárias e suficientes a coibir a obtenção, de forma individual ou coletiva, de benefícios ou vantagens pessoais, em decorrência da participação no respetivo processo decisório.</p>


        <p class="text-2xl text-nrp-blue py-4 text-center">DOS ASSOCIADOS:</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 5º</p>

        <p>Podem ser associados da “Na Rota dos Povos”, as pessoas singulares maiores de (18) dezoito anos e que declarem expressamente que concordam com os princípios orientadores, objetivos e fins da Associação;</p>
        <p>Podem também ser associados os menores cujos pais, tutores ou representantes legais sejam também associado</p>
        <p>Os menores que atinjam a maioridade terão que declarar expressamente que pretendem manter-se como associados e que concordam com os princípios orientadores, objetivos e fins da Associaçã</p>
        <p>Podem ser associados as pessoas coletivas, legalmente constituídas, cujos representantes legais, com poderes para o ato, declarem expressamente que concordam com os princípios orientadores, objetivos e fins da Associação; </p>
        <p>A admissão de novos associados será decidida pela Assembleia Geral, mediante proposta de um sócio efetivo ou da Direçã</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 6º</p>

        <p>A “Na Rota dos Povos” é constituída por número ilimitado de sócios, os quais serão das seguintes categorias: fundadores, honorários, efetivos e beneméritos.</p>
        <p>São sócios:</p>
        <p>FUNDADORES: os que participem no ato de constituição da associação.</p>
        <p>EFECTIVOS: as pessoas individuais, sem impedimento legal, que se proponham colaborar nos fins da associação, obrigando-se ao pagamento de joia e quota mensal nos montantes fixados pela Assembleia Geral, com base numa proposta da Direção.</p>
        <p>HONORÁRIOS: as pessoas individuais ou coletivas, que através de serviços ou contribuições promovam socialmente os fins da Associação, sendo tal distinção deliberada em Assembleia Geral, com base numa proposta da Direção ou da própria Assembleia Geral.</p>
        <p>BENEMÉRITOS/FIRQUIDJAS: as pessoas individuais ou coletivas, que ainda que não participando direta ou ativamente nas atividades da Associação, dão o seu contributo apoiando financeiramente estas atividades, ou doando em espécie para o prosseguimento das mesmas.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 7º</p>

        <p>Os associados, qualquer que seja a sua categoria, não respondem individualmente, solidária ou subsidiariamente pelas obrigações da “Na Rota dos Povos”, nem pelos atos praticados pela Direção.</p>



        <p class="text-xl text-nrp-green text-center pt-2">Artigo 8º</p>

        <p>São direitos dos associados efetivos e fundadores:</p>
        <ul class="list-disc font-thin space-y-1">
            <li class="list-item list-inside">Participar nas Assembleias Gerais;</li>
            <li class="list-item list-inside">Participar de todas as atividades associativas;</li>
            <li class="list-item list-inside">Eleger e serem eleitos para os cargos sociais;</li>
            <li class="list-item list-inside">Requerer a convocação de Assembleia Geral Extraordinária, com a assinatura de pelo menos metade mais um, dos associados;</li>
            <li class="list-item list-inside">Propor a criação e tomar parte em comissões e grupos de trabalho, quando designados para estas funções;</li>
            <li class="list-item list-inside">Apresentar propostas, programas e projetos de ação para a “Na Rota dos Povos”.</li>
            <li class="list-item list-inside">Ter acesso a todos os livros de natureza contabilística e financeira, bem como a todos os planos, relatórios, prestações de contas e resultados de auditoria independente.</li>
            <li class="list-item list-inside">Demitir-se.</li>
            <li class="list-item list-inside">Os direitos sociais previstos nestes Estatutos são pessoais e intransferíveis.</li>
        </ul>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 9º</p>

        <p>São deveres dos associados:</p>

        <ul class="list-disc font-thin space-y-1">
            <li class="list-item list-inside">Pagar atempadamente as suas quotas desde que sejam associados efetivos;</li>
            <li class="list-item list-inside">Acatar as deliberações dos Corpos Diretivos e das Assembleias Gerais</li>
            <li class="list-item list-inside">Desempenhar com zelo, dedicação e eficiência os cargos para que forem eleitos;</li>
            <li class="list-item list-inside">Cumprir as disposições estatutárias, cooperar para o desenvolvimento e maior prestígio da “Na Rota dos Povos” e difundir os seus objetivos e ações.</li>
            <li class="list-item list-inside">Os associados que não cumpram com os deveres estabelecidos ficam sujeitos às sanções previstas nestes Estatutos.</li>
        </ul>

        <p class="text-2xl text-nrp-blue py-4 text-center">DOS ORGÃOS SOCIAIS</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 10º</p>

        <p>São órgãos da Associação, a Assembleia Geral, a Direção, o Conselho Fiscal, eleitos em Assembleia Geral, convocada para o efeito, para um período de três (3) anos e o Conselho Superior e Consultivo.</p>



        <p class="text-xl text-nrp-green text-center pt-2">Artigo 11º</p>

        <p>Os membros eleitos dos órgãos sociais não serão remunerados no desempenho de funções inerentes aos cargos para os quais forem eleitos.</p>
        <p>Em casos de reconhecida necessidade, identificada pela Direção e reconhecida pela Assembleia Geral, os titulares dos órgãos sociais como os demais membros da Associação, poderão ser chamados a desempenhar funções executivas que, pela sua complexidade, e exigências de tempo e entrega, impliquem a sua remuneração.</p>
        <p>Seja ou não remunerado, o exercício de qualquer cargo pode justificar o pagamento das despesas dele derivadas.</p>


        <p class="text-2xl text-nrp-blue py-4 text-center">DA ASSEMBLEIA GERAL</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 12º</p>

        <p>A Assembleia Geral é o órgão soberano da “Na Rota dos Povos” e é constituída por todos os associados no pleno gozo dos seus direitos.</p>
        <p>A Assembleia Geral é presidida pela Mesa da Assembleia Geral.</p>
        <p>A Mesa da Assembleia Geral é composta por um Presidente, dois Vice-Presidentes e um Secretári</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 13º</p>

        <p>Compete, designadamente, à Mesa da Assembleia Geral:</p>

        <ul class="list-disc font-thin space-y-1">
            <li class="list-item list-inside">Convocar a sua realização.</li>
            <li class="list-item list-inside">Presidir e fiscalizar o processo eleitoral.</li>
            <li class="list-item list-inside">Dirigir, orientar e disciplinar os trabalhos da Assembleia Geral.</li>
            <li class="list-item list-inside">Conferir posse aos titulares dos cargos dos órgãos sociais.</li>
            <li class="list-item list-inside">Deliberar sobre a suspensão ou demissão de sócios.</li>
            <li class="list-item list-inside">Deliberar sobre as matérias que não sejam da competência exclusiva dos outros órgãos associação.</li>
        </ul>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 14º</p>

        <p>As assembleias reúnem-se em sessões ordinárias e extraordinárias;</p>
        <ul class="list-disc font-thin space-y-1">
            <li class="list-item list-inside">Ordinariamente no final de cada mandato, para eleição de novos corpos sociais e até trinta e um de março de cada ano para apreciação e votação do Relatório da Direção, Demonstração de Resultados e Balanço Anual e demais relatórios financeiros do exercício anterior, e o Orçamento e Plano Anual de Atividades para o novo exercício;</li>
            <li class="list-item list-inside">Extraordinariamente, apenas quando convocada pelo Presidente da Mesa da Assembleia Geral, Direção ou por pelo menos metade mais um dos Associados.</li>
        </ul>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 15º</p>

        <p>Tais reuniões serão convocadas com pelo menos quinze dias de antecedência sendo feita a divulgação destas através de email ou carta enviado para cada associado e onde serão indicados dia e hora, local e ordem de trabalhos.</p>
        <p>Se à hora marcada não estiverem presentes mais de metade dos associados, a mesma reunirá meia hora depois com os associados presentes.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 16º</p>

        <p>Na Assembleia Geral apenas podem exercer o seu voto os sócios fundadores e os sócios efetivos com as quotas em dia e que não se encontrem suspensos, condições também exigidas para eleger ou ser eleito.</p>
        <p>Os sócios honorários e beneméritos/firquidjas, podem estar presentes, como observadores, à Assembleia Ordinária Anual para apreciação e votação do Relatório da Direção, Demonstração de Resultados e Balanço Anual e demais relatórios financeiros do exercício anterior, e o Orçamento e Plano Anual de Atividades para o novo exercício.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 17º</p>

        <p>A Assembleia Geral não pode deliberar, em primeira convocação, sem pelo menos, a presença de metade mais um, dos seus associados.</p>
        <p>São tomadas por maioria absoluta dos associados presentes e com direito a voto, nomeadamente, as deliberações que versem sobre:</p>
        <ul class="list-disc font-thin space-y-1">
            <li class="list-item list-inside">Admissão de novos associados efetivos;</li>
            <li class="list-item list-inside">Nomeação dos membros dos Conselho Superior e Consultivo;</li>
            <li class="list-item list-inside">Deliberação sobre casos omissos e não previstos nestes Estatutos.</li>
            <li class="list-item list-inside">São tomadas com o voto favorável de três quartos do número de associados presentes e com direito a voto, as deliberações que versem sobre alterações dos estatutos;</li>
            <li class="list-item list-inside">São tomadas com o voto favorável de três quartos de todos os associados com direito a voto, as deliberações sobre a dissolução ou prorrogação da pessoa coletiva.</lu>
        </ul>

        <p class="text-2xl text-nrp-blue py-4 text-center">DA DIRECÇÃO</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 18º</p>

        <p>A “Na Rota dos Povos” será dirigida pela Direção.</p>
        <p>A Direção será composta por um número ímpar de titulares, no mínimo de sete e no máximo de onze, sendo um Presidente, dois Vice-Presidentes, um Secretário, um Tesoureiro e os restantes Vogais.</p>
        <p>Deverá apresentar ainda elementos suplentes em número mínimo de um, os quais se tornarão efetivos à medida que se verifique a vacatura de cargos efetivos neste órgão e pela ordem em que tiverem sido eleitos.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 19º</p>

        <p>A Direção da “Na Rota dos Povos”, deverá assumir as seguintes atribuições:</p>
        <ul class="list-disc font-thin space-y-1">
        <li class="list-item list-inside">Coordenar a gestão, funcionamento e administração da “Na Rota dos Povos” e dinamizar e impulsionar a sua atividade.</li>
        <li class="list-item list-inside">Garantir a boa implementação das linhas gerais da atividade da “Na Rota dos Povos” e propor eventuais revisões à deliberação da Assembleia Geral.</li>
        <li class="list-item list-inside">Elaborar e aprovar o regulamento interno, o organograma funcional e outros instrumentos previstos nos Estatutos ou outros necessários à organização da “Na Rota dos Povos” e à boa prossecução dos seus fins, cuja aprovação não esteja reservada a outro órgão.</li>
        <li class="list-item list-inside">Elaborar o Relatório e Contas do Exercício, submetê-lo ao parecer do Conselho Fiscal, e apresentá-lo para aprovação da Assembleia Geral.</li>
        <li class="list-item list-inside">Elaborar o Orçamento e Plano de Atividades para o ano civil seguinte, com o parecer obrigatório do Conselho Superior e Consultivo, para posterior apresentação à Assembleia Geral.</li>
        <li class="list-item list-inside">Elaborar reformas ou alterações aos presentes Estatutos, com o parecer obrigatório do Conselho Superior e Consultivo, para posterior apresentação à Assembleia Geral,</li>
        <li class="list-item list-inside">Propor à Assembleia Geral a criação de Delegações, Núcleos ou outras formas de representação social fora da sede.</li>
        <li class="list-item list-inside">Exercer o poder disciplinar sobre os associados.</li>
        <li class="list-item list-inside">Deliberar sobre a fusão, incorporação e extinção da “Na Rota dos Povos”, para posterior apresentação à Assembleia Geral, observando-se os presentes Estatutos quanto ao destino do seu património.</li>
        <li class="list-item list-inside">Abrir e movimentar as contas bancárias da “Na Rota dos Povos”.</li>
        <li class="list-item list-inside">Adquirir e registar os bens imóveis da Associação, após parecer obrigatório do Conselho Superior e Consultivo e com autorização expressa da Assembleia Geral.</li>
        <li class="list-item list-inside">Exercer o poder disciplinar sobre os funcionários.</li>
        <li class="list-item list-inside">Criar equipas específicas operacionais para a Gestão de cada Projeto, designadas Coordenação Operacional de Projeto (COP).</li>
        </ul>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 20º</p>

        <p>A Direção, no exercício das suas funções é auxiliada por equipas de Coordenação Operacional Projetos (COP), sendo as mesmas compostas por um coordenador e pelo menos por dois adjuntos.</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 21º</p>

        <p>As equipas de Coordenação Operacional de Projetos serão criadas pela Direção e coordenadas pela Comissão Executiva.</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 22º</p>

        <p>A cada equipa de Gestão de Projetos competirá, de acordo com o estabelecido no regulamento interno:</p>

        <ol>
            <li class="list-item list-inside">a) a elaboração anual ou, se possível, plurianual, do plano de atividades e orçamento </li>
            <li class="list-item list-inside">b) a elaboração trimestral de relatórios de acompanhamento.</li>
        </ol>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 23º</p>

        <p>O Presidente da “Na Rota dos Povos” visando imprimir maior operacionalidade às ações da Associação, poderá constituir uma Comissão Executiva com outros dois elementos da direção, e por si presidida, de modo a agilizar a gestão corrente da Associação, sem prejuízo de assumir as seguintes atribuições:</p>

        <ul class="list-disc font-thin space-y-1">
            <li class="list-item list-inside">Convocar, reunir e presidir às reuniões da Direção, pelo menos uma vez por mês; </li>
            <li class="list-item list-inside">Assinar, rubricar os termos de Abertura e Encerramento do Livro de Atas da Direção; </li>
            <li class="list-item list-inside">Coordenar e dirigir as atividades gerais específicas da “Na Rota dos Povos”;    </li>
            <li class="list-item list-inside">Celebrar convénios, protocolos e realizar a filiação da “Na Rota dos Povos” em instituições ou organizações, por delegação da Direção;  </li>
            <li class="list-item list-inside">Representar a “Na Rota dos Povos” em eventos, campanhas e reuniões, e demais atividades do interesse da Associação; </li>
            <li class="list-item list-inside">Exercer outras atribuições inerentes ao cargo, e não previstas expressamente nestes Estatutos;  </li>
            <li class="list-item list-inside">Representar a “Na Rota dos Povos” em Juízo e fora dele. </li>
        </ul>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 24º</p>

        <p>A Direção só poderá deliberar se estiverem presentes pelo menos três elementos.</p>
        <p>As deliberações são tomadas por maioria dos votos. </p>
        <p>Presidente da Direção tem voto de qualidade, em caso de empate.</p>


        <p class="text-2xl text-nrp-blue py-4 text-center">DO CONSELHO FISCAL</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 25º</p>

        <p>Ao Conselho Fiscal compete exercer a fiscalização das contas, vigiar pelo cumprimento da Lei, dos Estatutos, assistir às reuniões da Direção e dar parecer sobre o Relatório e Contas da Associação. </p>
        <p>Emitir parecer sobre qualquer matéria que envolva o património da “Na Rota dos Povos”, sempre que necessário e ainda emitir parecer sobre a dissolução e liquidação da associação.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 26º</p>

        <p>O Conselho Fiscal é composto por um Presidente, um Vice-Presidente e 3 Vogais.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 27º</p>

        <p>Compete ao Presidente convocar e presidir às reuniões, que devem ocorrer pelo menos uma vez por semestre.</p>
        <p>Compete ao Vice-Presidente coadjuvar e substituir o Presidente nas suas ausências e impedimentos.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 28º</p>

        <p>Compete aos vogais coadjuvar o 1º Vice-Presidente e substituí-lo nas suas ausências e impedimentos.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 29º</p>

        <p>O Conselho Fiscal só poderá deliberar se estiverem presentes a maioria dos titulares e as deliberações são tomadas por maioria dos votos, tendo o Presidente Voto de Qualidade.</p>


        <p class="text-2xl text-nrp-blue py-4 text-center">DO CONSELHO SUPERIOR E CONSULTIVO</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 30º</p>

        <p>O Conselho Superior e Consultivo da “Na Rota dos Povos”, tem como finalidade assessorar e apoiar a Direção, funcionários e colaboradores da associação, na prossecução dos seus objetivos estatutários e na elaboração, condução e implementação das suas ações, campanhas e projetos.</p>



        <p class="text-xl text-nrp-green text-center pt-2">Artigo 31º</p>

        <p>O Conselho Superior e Consultivo terá como membros pessoas individuais de reconhecido saber e idoneidade, nos campos de conhecimento afins com as atividades e fins da associação, e que com ela já tenham uma relação de participação ou envolvimento e serão escolhidos pelos sócios efetivos.</p>



        <p class="text-xl text-nrp-green text-center pt-2">Artigo 32º</p>

        <p>O Conselho Superior e Consultivo compor-se-á por no máximo quinze membros, mais o Presidente da Mesa da Assembleia Geral e os sócios honorários e sócios beneméritos/firquidjas que pretendam integrá-lo.</p>
        <p>Os membros deverão ser indicados de forma fundamentada pelos associados efetivos, nos termos do artigo 31º supra, aquando da eleição da Direção.</p>
        <p>O Conselho Superior e Consultivo terá um mandato de três (3) anos.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 33º</p>

        <p>Os membros do Conselho Superior e Consultivo elegerão, por maioria simples, o seu Presidente, que coordenará os trabalhos desse Conselho.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 34º</p>

        <p>O Conselho Superior e Consultivo reunir-se-á sempre que convocado pelo Presidente, devendo reunir-se pelo menos quadrimestralmente.</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 35º</p>

        <p>As deliberações do Conselho Superior e Consultivo são tomadas por maioria simples, cabendo ao seu Presidente o voto de qualidade.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 36º</p>

        <p>Os pareceres do Conselho Superior e Consultivo, quando obrigatórios, não têm natureza vinculativa para o Órgão Social que o solicitou.</p>



        <p class="text-2xl text-nrp-blue py-4 text-center">FORMA DE OBRIGAR</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 37º</p>

        <p>A “Na Rota dos Povos” obriga-se pela assinatura de dois membros da Direção, um deles obrigatoriamente o Presidente da Direção, não podendo, todavia, ser as de dois elementos entre os quais haja laços familiares diretos entre si;</p>
        <p>No que respeita aos atos de mero expediente basta a assinatura de qualquer membro da Direção.    </p>
        <p>A constituição legal de mandatários, por via da outorga das respetivas procurações só poderá ser efetuada mediante a aprovação em reunião de Direção, da qual deve ser lavrada a respetiva ata.  </p>


        <p class="text-2xl text-nrp-blue py-4 text-center">DAS RECEITAS E DO PATRIMÓNIO</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 38º</p>

        <p>São receitas da “Na Rota dos Povos”, entre outras:</p>
        <ul class="list-disc font-thin space-y-1">
            <li class="list-item list-inside">As quotas dos membros associados;  </li>
            <li class="list-item list-inside">Os rendimentos de bens próprios;   </li>
            <li class="list-item list-inside">As doações, legados, heranças e respetivos rendimentos;</li>
            <li class="list-item list-inside">Os subsídios, donativos, comparticipações e financiamentos de que seja beneficiária;   </li>
            <li class="list-item list-inside">O produto de subscrições e das suas atividades;</li>
            <li class="list-item list-inside">As receitas de iniciativas de angariação de fundos, como coletas;  </li>
            <li class="list-item list-inside">As receitas de espetáculos, colóquios ou outros eventos e iniciativas que sejam promovidas com essa finalidade;</li>
            <li class="list-item list-inside">As receitas que, no estrito respeito pelo enquadramento legal e fiscal em vigor, venham a ser adquiridas no âmbito de iniciativas de comercialização de bens e serviços, incluindo ao nível da consultoria, outsourcing, formação, venda de produtos solidários, comércio justo, participação em produtos de investimento solidário, sempre que sejam realizadas como atividades acessórias e subsidiárias, realizadas em função da necessidade de obtenção de recursos próprios para o cumprimento da missão e objetivos sociais da Na Rota dos Povos;</li>
            <li class="list-item list-inside">Quaisquer outras receitas permitidas por lei.  </li>
            <li class="list-item list-inside">A “Na Rota dos Povos” não poderá receber qualquer tipo de doação ou subvenção que possa comprometer os seus princípios.</li>
        </ul>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 39º</p>

        <p>O património da “Na Rota dos Povos” será constituído por doações de pessoas individuais e/ou coletivas, de direito público ou privado, nacionais e estrangeiras e por aquisições.</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 40º</p>

        <p>A “Na Rota dos Povos” não distribuirá, entre os seus sócios, associados, conselheiros, diretores, empregados ou doadores, eventuais excedentes operacionais, brutos ou líquidos, dividendos, bonificações, participações ou parcelas do seu património.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 41º</p>

        <p>A “Na Rota dos Povos” aplicará integralmente as suas receitas, recursos e eventual resultado operacional na manutenção e desenvolvimento dos objetivos institucionais.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 42º</p>

        <p>É expressamente proibido o uso da denominação social em atos que envolvam a “Na Rota dos Povos” em obrigações relativas a negócios estranhos ao seu objetivo social, especialmente a prestação de avais, endossos, fianças e caução de favor.</p>



        <p class="text-xl text-nrp-green text-center pt-2">Artigo 43º</p>

        <p>Aprovada a extinção da “Rota dos Povos”, e no âmbito do seu processo de dissolução, o destino do seu património, será decidido em Assembleia Geral, convocada para esse fim, devendo obrigatoriamente tal património ser destinado a uma instituição que se reja pelos mesmos princípios e tenha os mesmos fins que a “Na Rota dos Povos”, que demonstre capacidade humana e financeira comprovada para assegurar a continuidade do projeto “Casa Mamé” e do “Centro de Educação Especial e Terapêutica”.</p>
        <p>Caso não seja possível selecionar uma instituição com os requisitos supra elencados, o património deverá ser alienado e o produto da sua venda, destinado a assegurar o futuro das crianças que ainda estejam aos cuidados da “Casa Mamé”</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 44º</p>

        <p>A “Na Rota dos Povos” observará as normas de prestação de contas, que determinarão, no mínimo:</p>

        <ul class="list-disc font-thin space-y-1">
            <p>A observância dos princípios fundamentais de contabilidade e das Normas Portuguesas de Contabilidade;</p>
            <p>Que se dê publicidade por qualquer meio eficaz, no encerramento do exercício fiscal, ao relatório de atividades e das demonstrações financeiras da entidade.</p>
        </ul>

        <p class="text-2xl text-nrp-blue py-4 text-center">DAS INFRACÇÕES DISCIPLINARES E RESPECTIVOS PROCEDIMENTOS</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 45º</p>

        <p class="text-center">(Infração Disciplinar)</p>
        <br>
        <p>Comete infração disciplinar o associado que, por ação ou omissão, violar dolosa ou culposamente algum dos deveres decorrentes dos Estatutos ou Regulamentos da Associação.</p>



        <p class="text-xl text-nrp-green text-center pt-2">Artigo 46º</p>

        <p class="text-center">(Penas Disciplinares)</p>
        <br>
        <p>As penas disciplinares são as seguintes:</p>
        <ul class="list-disc font-thin space-y-1">
        <li class="list-item list-inside">Advertência;</li>
        <li class="list-item list-inside">Repreensão registada;</li>
        <li class="list-item list-inside">Suspensão Temporária;</li>
        <li class="list-item list-inside">Expulsão.</li>
        </ul>

        <p>As penas previstas nas alíneas c) e d) só podem ser aplicadas por infração disciplinar que viole as normas estatutárias que tutelam os princípios fundamentais pelos quais se rege a Associação e sempre que a sua idoneidade, independência ou património fiquem afetados, devendo a decisão ser tomada por, no mínimo, dois terços dos votos de todos os membros da Direção.</p>
        <p class="text-xl text-nrp-green text-center pt-2">As penas previstas nas alíneas a) e b) deste artigo são antecedidas de averiguação simples e rápida, sem formalidades especiais.</p>
        <p>As penas previstas nas alíneas c) e d) são precedidas de processo disciplinar.</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 47º</p>

        <p class="text-center">(Competência Disciplinar)</p>
        <br>
        <p>O poder disciplinar é exercido pela Direção, o qual pode nomear pessoas idóneas ou advogados para, por delegação, procederem a averiguações e ou fazerem a instrução dos processos disciplinares. </p>



        <p class="text-xl text-nrp-green text-center pt-2">Artigo 48º</p>

        <p class="text-center">(Competência Disciplinar da Assembleia Geral)</p>
        <br>
        <p>A Assembleia Geral exerce o poder disciplinar relativamente aos atuais e antigos titulares dos órgãos da Associação.</p>
        <p>Compete à Assembleia Geral julgar, em última instância, os recursos das deliberações do Direção relativamente às infrações disciplinares, nos casos previstos nestes Estatutos. </p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 49º</p>
        <br>
        <p class="text-center">(Tramitação)</p>
        <br>
        <p>O procedimento disciplinar terá por base decisão do Presidente da Direção e é instaurado com fundamento em participação dirigida à “Na Rota dos Povos”, por qualquer pessoa devidamente identificada que tenha conhecimento de factos suscetíveis de integrarem infração disciplinar.</p>
        <p>O Presidente da Direção pode, independentemente de participação, ordenar a instauração de procedimento disciplinar.</p>
        <p>As pessoas com interesse direto relativamente aos factos participados são admitidas a intervir no processo, sendo notificadas por escrito, da participação de que foram alvo, podendo no prazo de dez dias, apresentarem defesa, indicando todos os meios de prova, permitidos por lei, com um limite de 3 testemunhas;</p>
        <p>A tramitação do procedimento disciplinar deve ser sumária e, através dela, deve o relator tentar atingir a verdade material, remover os obstáculos ao seu regular e rápido andamento e recusar o que for impertinente, inútil ou dilatório.</p>
        <p>Na falta de disposição especial, será de 10 dias o prazo para a prática dos atos, no âmbito do procedimento disciplinar.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 50º</p>

        <p class="text-center">(Decisão)</p>
        <br>
        <p>Terminada a instrução, o relator profere despacho de acusação ou emite parecer fundamentado a propor o arquivamento do processo ou que este aguarde produção de melhor prova, e apresenta o processo à Direção para deliberação.</p>
        <p>A deliberação da Direção deve ser fundamentada.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 51º</p>

        <p class="text-center">(Notificação)</p>
        <br>
        <p>A decisão final do processo é notificada ao arguido e aos interessados, por carta registada com aviso de receção.</p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 52º</p>
        <br>
        <p class="text-center">(Prazo para Termo do Processo Disciplinar)</p>
        <br>
        <p>Os processos disciplinares devem ser instruídos e apresentados à Direção para deliberação, no prazo máximo de três meses, o qual poderá ser prorrogado por igual período pelo Presidente da Direção, ocorrendo motivo que o justifique.</p>



        <p class="text-xl text-nrp-green text-center pt-2">Artigo 53º</p>

        <p class="text-center">(Recursos)</p>
        <br>
        <p>Das deliberações da Direção que determinem as penas de suspensão cabe recurso, com efeito suspensivo, para a primeira Assembleia Geral a realizar.</p>
        <p>Das deliberações da Direção que determinem a pena de expulsão cabe recurso, sem efeito suspensivo, para a primeira Assembleia Geral a realizar.</p>
        <p>Têm legitimidade para interpor recurso o arguido, os interessados e o Presidente da Direção.</p>
        <p>O prazo de interposição do recurso é de 15 dias, a contar da notificação.</p>
        <p>O Presidente da Direção pode recorrer, no prazo de 15 dias, ou mandando seguir o recurso mediante simples despacho.</p>
        <p>Admitido o recurso que subir imediatamente, devem recorrente e recorrido, apresentar as suas alegações em prazos sucessivos de 15 dias, sendo-lhes para tanto facultada a consulta do processo.</p>
        <p>Julgado, definitivamente qualquer recurso, o processo é enviado para a Direção.</p>


        <p class="text-xl text-nrp-green text-center pt-2">Artigo 54º</p>

        <p class="text-center">(Processo de Inquérito)</p>
        <br>
        <p>Pode ser ordenada a abertura de processo de inquérito, sempre que não esteja concretizada a infração ou não seja conhecido o infrator, e ainda, quando se torne necessário proceder a averiguações para o esclarecimento dos facto</p>
        <p>O processo de inquérito regula-se pelas normas aplicáveis ao processo disciplinar.</p>
        <p>Finda a instrução, o relator apresenta à Direção o seu parecer fundamentado em que proponha ou o prosseguimento do processo como processo disciplinar ou o seu arquivamento, consoante considere que existem ou não indícios. </p>

        <p class="text-xl text-nrp-green text-center pt-2">Artigo 55º</p>

        <p>Para os casos omissos em matéria estatutária tem aplicação a Lei Civil.</p>

